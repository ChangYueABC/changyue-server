package com.entity;

public class VolumeInfo {
	private String volumeId;
	private String volumeName;
	private String seriseId;
	private String period;
	private String booksNum;
	private String level;
	
	public String getVolumeId() {
		return volumeId;
	}
	public void setVolumeId(String volumeId) {
		this.volumeId = volumeId;
	}
	
	public String getVolumeName() {
		return volumeName;
	}
	public void setVolumeName(String volumeName) {
		this.volumeName = volumeName;
	}
	
	public String getSeriseId() {
		return seriseId;
	}
	public void setSeriseId(String seriseId) {
		this.seriseId = seriseId;
	}
	
	public String getPeriod() {
		return period;
	}
	public void setPeriod(String period) {
		this.period = period;
	}
	
	public String getBooksNum() {
		return booksNum;
	}
	public void setBooksNum(String booksNum) {
		this.booksNum = booksNum;
	}
	
	public String getLevel() {
		return level;
	}
	public void setLevel(String level) {
		this.level = level;
	}

}
